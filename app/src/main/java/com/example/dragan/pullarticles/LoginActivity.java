    private class Task extends AsyncTask<Void,Void,String>{
        @Override
        protected String doInBackground(Void... params) {
            JSONObject object=new JSONObject();
            String text = "";
            try {
                URL urlcheck = new URL("http://android.ogosense.net/interns/ace/login.php");
                HttpURLConnection con = (HttpURLConnection) urlcheck.openConnection();
                if (con.getResponseCode() != 200) {
                    text = -2+"";
                }else {


                    object.put("username", username);
                    object.put("password", pw);
                    URL url = new URL("http://android.ogosense.net/interns/ace/login.php");
                    HttpURLConnection conn = (HttpURLConnection) url.openConnection();

                    conn.setDoOutput(true);
                    conn.setDoInput(true);
                    conn.setRequestProperty("Content-Type", "application/json");
                    conn.setRequestProperty("Accept", "application/json");
                    conn.setRequestMethod("POST");
                    OutputStreamWriter wr = new OutputStreamWriter(conn.getOutputStream());
                    wr.write(object.toString());
                    wr.flush();
                    BufferedReader reader = new BufferedReader(new InputStreamReader(conn.getInputStream()));

                    StringBuilder sb = new StringBuilder();
                    String line;

                    // Read Server Response
                    while ((line = reader.readLine()) != null) {
                        // Append server response in string
                        sb.append(line).append("\n");
                    }
                    text = sb.toString();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            return text;
        }

        @Override
        protected void onPostExecute(String s) {
            super.onPostExecute(s);

            if(s.equals("-2")) {
                Toast.makeText(getApplicationContext(), "Server unreachable", Toast.LENGTH_SHORT).show();
                Intent loginAgain = new Intent(LoginActivity.this, LoginActivity.class);
                LoginActivity.this.finish();
                startActivity(loginAgain);

            }else {

                try {
                    JSONObject response = new JSONObject(s);
                    int uid = response.getInt("uid");
                    if (uid > -1) {
                        Intent loginIntent = new Intent(LoginActivity.this, ListOfArticles.class);
                        loginIntent.putExtra("uid", String.valueOf(uid));
                        LoginActivity.this.finish();
                        startActivity(loginIntent);
                    } else {
                            Toast.makeText(getApplicationContext(), "Incorrect username or password", Toast.LENGTH_LONG).show();
                            Intent loginAgain = new Intent(LoginActivity.this, LoginActivity.class);
                            LoginActivity.this.finish();
                            startActivity(loginAgain);

                    }
                } catch (JSONException e) {
                    e.printStackTrace();
                }
            }
        }
    }

